//ডাৰ্ট (Dart) ত **অৱ্য়াহতি পৰিসৰ (Optional Parameters)** হৈছে এক প্ৰকাৰৰ পৰিসৰ (parameter) যিবোৰ ব্যৱহাৰকাৰী কিম্বা কলাৰ পৰা নিৰ্বাচিতকৈ অন্তৰ্ভুক্ত নকৰিবলৈ সক্ষম। এইটো মেইথড বা ফাংশন কল কৰাৰ সময়ত অনুবন্ধিত নহ'লে মান ব্যৱহাৰ কৰা হয়। ই কোডৰ নমনীয়তা আৰু সৰলতা বৃদ্ধি কৰে, আৰু কিছুমান পৰিসৰ ঐচ্ছিক কৰি ব্যৱহাৰকাৰীলৈ অধিক নিয়ন্ত্ৰণ প্ৰদান কৰে।
//
//ডাৰ্টত দুই ধৰণৰ **অৱ্য়াহতি পৰিসৰ**ৰ ব্যৱহাৰ হয়:
//1. **নামিত অব্যাহত পৰিসৰ (Named Optional Parameters)**
//2. **অসংখ্য অব্যাহত পৰিসৰ (Positional Optional Parameters)**
//
//আৰম্ভ কৰোঁ, এইবোৰৰ প্ৰতিটো বিস্তারিত ব্যাখ্যা:
//
//### ১. **নামিত অব্যাহত পৰিসৰ (Named Optional Parameters)**
//
//নামিত অব্যাহত পৰিসৰত, তুমি ফাংশন বা মেথডৰ পৰিসৰসমূহক নাম দিয়াৰ মাধ্যমে পৰিচয় কৰি দিব পাৰা। এই পৰিসৰবোৰ কলৰ সময়ত ঐচ্ছিক হৈ থাকে, আৰু কলৰ সময়ত প্ৰতিটো পৰিসৰ খালী ৰখা যাব পাৰে।
//
//**ব্যাখ্যা:**
//- নামিত অব্যাহত পৰিসৰসমূহ `{}` ৰ মাজত লিখা হয়।
//- এই পৰিসৰসমূহ পৰিচিত (named) থাকে, আৰু তুমি যিকোনো পৰিসৰ প্ৰথমে বা পাছত উল্লেখ কৰিব পাৰা, যাৰ মান প্ৰদৰ্শন কৰা নাযায়।
//
//**উদাহৰণ:**
//```dart
//void greet({String? name, int? age}) {
//print("Hello, $name!");
//if (age != null) {
//print("You are $age years old.");
//}
//}
//
//void main() {
//greet(name: 'John');  // নাম দিলো, বয়স উল্লেখ কৰা নহ’ল।
//greet(name: 'Alice', age: 30);  // নাম আৰু বয়স দুইটা মানে দিলো।
//}
//```
//
//**ব্যাখ্যা:**
//- `greet()` মেথডটোত `{}` ৰ ভিতৰত দুটা ঐচ্ছিক পৰিসৰ (parameters) আছে: `name` আৰু `age`। এইবোৰ ঐচ্ছিক, অৰ্থাৎ কলৰ সময়ত, তুমি যিকোনো এক বা দুয়োটাও প্ৰদান কৰিব পাৰা।
//- কলৰ সময়ত, নাম আৰু বয়স উল্লেখ কৰা হৈছে, আৰু যদি বয়সৰ মান নাই থাকে তেন্তে `null` মান সুৰক্ষিত থাকে।
//
//### ২. **অসংখ্য অব্যাহত পৰিসৰ (Positional Optional Parameters)**
//
//অসংখ্য অব্যাহত পৰিসৰত, পৰিসৰবোৰ `[]` ৰ ভিতৰত লিখা হয়। এই পৰিসৰবোৰ মেথড বা ফাংশন কলৰ সময়ত ঐচ্ছিক হৈ থাকে, আৰু যিকোনো পৰিসৰ পজিশনৰ ভিত্তিত কল কৰিব পৰা যায়। যেতিয়া কোনো ঐচ্ছিক পৰিসৰ অন্তর্ভুক্ত কৰা নহ'লে, সেয়ে `null` বুলি ধৰা হয় বা ডিফল্ট মান প্ৰয়োগ কৰা হয়।
//
//**ব্যাখ্যা:**
//- পজিশনাল অপশ্যনাল পৰিসৰসমূহ `[]` ৰ ভিতৰত থাকে।
//- কলৰ সময়ত ঐচ্ছিক পৰিসৰসমূহৰ মান প্রদান কৰা নাযায়, তেন্তে সেই পৰিসৰ সমূহ `null` হিচাপে ধৰা হয়।
//
//**উদাহৰণ:**
//```dart
//void printDetails(String name, [int? age, String? city]) {
//print("Name: $name");
//if (age != null) {
//print("Age: $age");
//}
//if (city != null) {
//print("City: $city");
//}
//}
//
//void main() {
//printDetails('John');  // মাত্ৰ নামটো দিছোঁ
//printDetails('Alice', 25);  // নাম আৰু বয়স দিছোঁ
//printDetails('Bob', 30, 'New York');  // নাম, বয়স আৰু চহৰ দিছোঁ
//}
//```
//
//**ব্যাখ্যা:**
//- `printDetails()` মেথডটোত `name` এটা প্ৰয়োজনীয় পৰিসৰ আৰু `age` আৰু `city` ঐচ্ছিক পজিশনাল পৰিসৰ।
//- প্ৰথম কলত, মাত্ৰ `name` দিয়া হৈছে, বাকী পৰিসৰবোৰ `null` হিচাপে ধৰা হৈছে।
//- দ্বিতীয় কলত, `age` পৰিসৰও অন্তৰ্ভুক্ত কৰা হৈছে, আৰু তৃতীয় কলত, তিনিওটা পৰিসৰ মান প্ৰদান কৰা হৈছে।
//
//### ৩. **ডিফল্ট মান (Default Values)**
//ডাৰ্টত, তুমি ঐচ্ছিক পৰিসৰৰ বাবে ডিফল্ট মান (default value) প্ৰদান কৰিব পাৰা। ই যেতিয়া কলৰ সময়ত ঐচ্ছিক পৰিসৰ মান অন্তর্ভুক্ত কৰা নহ'লে তেন্তে সেই ডিফল্ট মানটো ব্যৱহাৰ কৰা হয়।
//
//**উদাহৰণ:**
//```dart
//void displayInfo({String name = 'Guest', int age = 0}) {
//print("Name: $name");
//print("Age: $age");
//}
//
//void main() {
//displayInfo();  // নাম আৰু বয়সৰ ডিফল্ট মান ব্যৱহাৰ কৰিব
//displayInfo(name: 'Alice');  // মাত্ৰ নামটো দিয়াৰ পিছত বয়সৰ ডিফল্ট মান
//displayInfo(name: 'Bob', age: 30);  // নাম আৰু বয়স দিয়া হৈছে
//}
//```
//
//**ব্যাখ্যা:**
//- `name` আৰু `age` পৰিসৰসমূহৰ বাবে ডিফল্ট মান উল্লেখ কৰা হৈছে।
//- যেতিয়া এই পৰিসৰসমূহ কলৰ সময়ত নিৰ্দিষ্ট কৰা নহ'লে, তেন্তে সেয়া ডিফল্ট মান (যেনে `'Guest'` আৰু `0`) ব্যৱহাৰ কৰা হয়।
//
//### **মুঠতে, ডাৰ্টৰ ঐচ্ছিক পৰিসৰ ব্যৱহাৰ**
//1. **নামিত অব্যাহত পৰিসৰ (Named Optional Parameters)** `{}` ৰ ভিতৰত থাকে আৰু কলৰ সময়ত যিকোনো পৰিসৰ উল্লেখ কৰিব পাৰা।
//2. **অসংখ্য অব্যাহত পৰিসৰ (Positional Optional Parameters)** `[]` ৰ ভিতৰত থাকে আৰু পৰিসৰৰ মান প্রদান কৰাৰ পদ্ধতি পজিশন অনুসৰি।
//3. **ডিফল্ট মান** ব্যৱহাৰ কৰি, পৰিসৰসমূহৰ বাবে মান নিৰ্দিষ্ট কৰা যায়।
//
//এই পৰিসৰসমূহৰ ব্যৱহাৰ কোডক অধিক নমনীয় আৰু সুসংহত কৰে, আৰু কাৰ্যকলাপৰ ক্ষেত্ৰত অধিক সুবিধা প্ৰদান কৰে।