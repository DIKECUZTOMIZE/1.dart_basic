//*ব্যাখ্যা:
//Flyable মিক্সিন: Flyable মিক্সিনত fly() মেথড আছে, যি সকলো ক্লাছত অন্তৰ্ভুক্ত কৰিব পাৰি যিসকল ক্লাছ উৰিব পৰা যায়।
//Swimmable মিক্সিন: Swimmable মিক্সিনত swim() মেথড আছে, যি ক্লাছসমূহত অন্তৰ্ভুক্ত কৰিব পাৰি যিসকল ক্লাছ সাঁতৰাব পাৰে।
//Bird ক্লাছ: Bird ক্লাছটো Flyable মিক্সিনৰ পৰা মেথড fly() গ্ৰহণ কৰিছে আৰু নিজস্ব মেথড chirp()ৰ লগতে এটা নতুন বাচলৰ পৰা গুণাবলী লাভ কৰিছে।
//Fish ক্লাছ: Fish ক্লাছটো Swimmable মিক্সিনৰ পৰা মেথড swim() গ্ৰহণ কৰিছে আৰু নিজস্ব মেথড jump()ৰ সৈতে সাঁতৰা আৰু লাফ দিয়া।


//*মিক্সিনৰ সুবিধাসমূহ:
//কোড পুনঃব্যৱহাৰ (Code Reuse): একাধিক ক্লাছৰ গুণাবলী মিক্সিনৰ মাধ্যমে একে ক্লাছত অন্তৰ্ভুক্ত কৰিব পৰা যায়, যাৰ ফলত কোড পুনঃব্যৱহাৰ আৰু মডুলাৰিটি বৃদ্ধি পায়।
//মাল্টিপল ইনহেৰিটেন্সৰ বিকল্প: ডাৰ্টত মাল্টিপল ইনহেৰিটেন্সৰ অনুমতি নাই, কিন্তু মিক্সিনৰ মাধ্যমে আপুনি একাধিক ফিচাৰ একে ক্লাছত অন্তৰ্ভুক্ত কৰিব পাৰে, যাৰ ফলে মাল্টিপল ইনহেৰিটেন্সৰ অভাৱ পূৰ্ণ হয়।
//ক্লীন কোড: মিক্সিনে কোডৰ গঠন অধিক পৰিষ্কৃত আৰু পুনঃব্যৱহাৰযোগ্য কৰি তোলে। একে মেথড বা গুণাবলী একাধিক ক্লাছত ব্যৱহাৰ কৰিবলৈ, ক্লাছসমূহক মিক্সিনৰ সাহায্যে সহজে একে সময়তে সংযুক্ত কৰা যায়।


//*মিক্সিনৰ সীমাবদ্ধতা:
//অবজেক্ট নাথাকিব পাৰে: মিক্সিনৰ নিজস্ব অবজেক্ট (instance) নাথাকে, এই বাবে মিক্সিনৰ মেথডসমূহ কেৱল ক্লাছৰ সৈতে মিশ্ৰিত হৈ থাকে।
//একাধিক মিক্সিনৰ মিশ্ৰণ: একে সময়তে একাধিক মিক্সিন মিক্স কৰা যায়, কিন্তু যদি একাধিক মিক্সিনৰ ভিতৰত একে নামৰ মেথড থাকে, তেন্তে তাতে কনফ্লিক্টৰ সৃষ্টি হ'ব পাৰে।


