//*ডার্ট (Dart) ভাষাত Mixin হৈছে এক শক্তিশালী বৈশিষ্ট্য যাৰ মাধ্যমে আপুনি একাধিক ক্লাছৰ গুণাবলী (properties) আৰু মেথড (methods) এক ক্লাছত অন্তৰ্ভুক্ত কৰিব পাৰে। মিক্সিনৰ সাহায্যে একে সময়ত একাধিক ক্লাছৰ পৰা ফিচাৰ গ্ৰহণ কৰি কোড পুনঃব্যৱহাৰ সহজ হয়। মিক্সিনৰ মাধ্যমে, ক্লাছ ইনহেৰিটেন্সৰ সীমাবদ্ধতা অতিক্রম কৰিব পৰা যায় আৰু কোডৰ গঠন অধিক পৰিষ্কৃত আৰু পুনঃব্যৱহাৰযোগ্য হয়।

//*1. মিক্সিন কি?
//মিক্সিন হৈছে এক বিশেষ পদ্ধতি, য'ত আপুনি এক বা একাধিক ক্লাছৰ পৰা ফিচাৰ (methods, properties) গ্ৰহণ কৰিব পাৰে। কিন্তু, এটি মিক্সিনৰ নিজস্ব ইনস্টেন্স (instance) বা অবজেক্ট (object) নাথাকিব পাৰে, অৰ্থাৎ মিক্সিনৰ মুল উদ্দেশ্য হৈছে মেথড আৰু গুণাবলী এক ক্লাছত যোগ কৰা।

//ডার্টত মিক্সিন ব্যৱহাৰ কৰাৰ মূল সুবিধা হৈছে যে আপুনি একে সময়ত একাধিক মিক্সিনৰ মেথড আৰু গুণাবলী এক ক্লাছত অন্তৰ্ভুক্ত কৰিব পাৰে, যাৰ ফলে একাধিক ইনহেৰিটেন্সৰ পৰা ফিচাৰ লাভ কৰা হয়।



